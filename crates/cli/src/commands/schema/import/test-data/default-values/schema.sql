CREATE TABLE issues (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    description TEXT NOT NULL DEFAULT 'No description',
    status VARCHAR(50) NOT NULL DEFAULT 'pending',
    price DECIMAL(10, 2) NOT NULL DEFAULT 10.00,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    due_date DATE NOT NULL DEFAULT CURRENT_DATE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    fixed_date TIMESTAMP NOT NULL DEFAULT '2025-01-01 12:00:00',
    fixed_time TIME NOT NULL DEFAULT '12:00:00',
    fixed_timestamp TIMESTAMP NOT NULL DEFAULT '2025-01-01 12:00:00',
    fixed_int INT NOT NULL DEFAULT 100,
    fixed_float FLOAT NOT NULL DEFAULT 100.00
);